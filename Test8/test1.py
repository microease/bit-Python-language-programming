# 描述
# 获得用户的任何可能输入，将其中的英文字符进行打印输出，程序不出现错误。‪‬‪‬‪‬‪‬‪‬‮‬‪‬‫‬‪‬‪‬‪‬‪‬‪‬‮‬‫‬‭‬‪‬‪‬‪‬‪‬‪‬‮‬‪‬‫‬‪‬‪‬‪‬‪‬‪‬‮‬‪‬‮‬‪‬‪‬‪‬‪‬‪‬‮‬‪‬‭‬‪‬‪‬‪‬‪‬‪‬‮‬‫‬‪‬
#
# 输入输出示例
# 输入
# 输出
# 示例
# 1
# * & ^ 123
# abc0e
# abce

# !/usr/bin/python
# -*- coding: UTF-8 -*-


s = input()
for c in s:
    c = str(c)
    if 'z' >= c >= 'a' or 'Z' >= c >= "A":
        print(c, end="")